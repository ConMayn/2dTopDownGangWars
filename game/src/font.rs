/// UV-koordinater for et tegn i atlas.
/// Returnerer (u0, v0, u1, v1) i 0.0-1.0 range.
pub fn char_uv(c: char) -> Option<(f32, f32, f32, f32)> {
    let idx = char_index(c)?;
    let col = idx % COLS;
    let row = idx / COLS;
    let u0 = col as f32 * CHAR_W as f32 / ATLAS_W as f32;
    let v0 = row as f32 * CHAR_H as f32 / ATLAS_H as f32;
    let u1 = (col + 1) as f32 * CHAR_W as f32 / ATLAS_W as f32;
    let v1 = (row + 1) as f32 * CHAR_H as f32 / ATLAS_H as f32;
    Some((u0, v0, u1, v1))
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn atlas_has_white_pixels() {
        let atlas = generate_font_atlas();
        let white_count = atlas.pixels().filter(|p| p[0] == 255 && p[3] == 255).count();
        assert!(white_count > 50, "font atlas should have visible glyphs");
    }

    #[test]
    fn char_uv_for_a() {
        let (u0, v0, u1, v1) = char_uv('A').unwrap();
        assert!(u0 < u1);
        assert!(v0 < v1);
    }
}