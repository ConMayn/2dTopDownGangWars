//! Font — procedural bitmap font til tekst-rendering.
//!
//! Genererer en 8x8 bitmap font for A-Z, 0-9, og tegnsætning.
//! Fonten er en enkelt texture atlas der bruges af TextRenderer.

use image::{ImageBuffer, Rgba, RgbaImage};

/// Antal tegn per række i atlas.
const COLS: u32 = 16;
/// Tegn per kolonne i atlas.
const ROWS: u32 = 8;
/// Pixel-størrelse per tegn.
pub const CHAR_W: u32 = 8;
pub const CHAR_H: u32 = 8;
/// Atlas dimensioner.
pub const ATLAS_W: u32 = COLS * CHAR_W; // 128
pub const ATLAS_H: u32 = ROWS * CHAR_H; // 64

/// Tegn-rækkefølge i atlas (16 per række, 8 rækker = 128 tegn).
const CHARSET: &[u8] = b" !\"#$%&'()*+,-./0123456789:;<=>?@ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_`abcdefghijklmnopqrstuvwxyz{|}~";

/// Hent atlas-index for et tegn.
pub fn char_index(c: char) -> Option<u32> {
    let b = c as u8;
    CHARSET.iter().position(|&ch| ch == b).map(|i| i as u32)
}

/// 8x8 bitmap tegn-data (1 byte per række, bit 7 = venstre pixel).
/// Hver tegn er 8 rækker = 8 bytes.
type Glyph = [u8; 8];

/// Generer en simpel 8x8 font. Returnerer atlas som RgbaImage.
pub fn generate_font_atlas() -> RgbaImage {
    let mut atlas = ImageBuffer::new(ATLAS_W, ATLAS_H);
    let white = Rgba([255, 255, 255, 255]);
    let transparent = Rgba([0, 0, 0, 0]);

    // Clear atlas.
    for p in atlas.pixels_mut() {
        *p = transparent;
    }

        for (i, &ch) in CHARSET.iter().enumerate() {
            let glyph = get_glyph(ch);
            let col = (i as u32) % COLS;
            let row = (i as u32) / COLS;
            let x0 = col * CHAR_W;
            let y0 = row * CHAR_H;
            for gy in 0u32..8 {
                let bits = glyph[gy as usize];
                for gx in 0u32..8 {
                    if bits & (0x80 >> gx) != 0 {
                        atlas.put_pixel(x0 + gx, y0 + gy, white);
                    }
                }
            }
        }

    atlas
}

